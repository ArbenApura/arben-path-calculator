<script lang="ts">
	// IMPORTED STORES
	import { entities } from '$lib/stores/entities';
	// IMPORTED DEP-UTILS
	import panzoom, { type PanZoom } from 'panzoom';
	import isMobile from 'is-mobile';
	// IMPORTED COMPONENTS
	import EntityContainer from './EntityContainer.svelte';
	import { Button } from '$lib/shadcn';

	// -- STATES -- //

	let instance: PanZoom;
	let isNavigate = true;

	// -- UTILS -- //

	const initPanzoom = (node: HTMLDivElement) => {
		instance = panzoom(node, {
			initialZoom: 1,
			minZoom: 0.5,
			maxZoom: 2
		});

		if (isMobile()) {
			instance.pause();
			isNavigate = false;
		}
	};

	const handleToggle = () => {
		if (instance) {
			if (instance.isPaused()) {
				instance.resume();
				isNavigate = true;
			} else {
				instance.pause();
				isNavigate = false;
			}
		}
	};
</script>

<!-- NAVIGATE BUTTON -->
<Button
	class="fixed bottom-4 left-4 z-20 flex items-center gap-2 shadow-sm"
	variant={isNavigate ? 'default' : 'outline'}
	on:click={handleToggle}
>
	<i class="ph-bold ph-hand-pointing"></i>
	{isNavigate ? 'Navigating' : 'Navigate'}
</Button>

<!-- CALCULATE BUTTON -->
<Button class="fixed bottom-4 right-4 z-20 flex items-center gap-2 shadow-sm">
	<i class="ph-bold ph-play"></i>
	Calulate
</Button>

<div class="fixed left-0 top-0 h-full w-full overflow-hidden">
	<div class="panzoom flex h-full w-full justify-center p-4" use:initPanzoom>
		{#each $entities as entity (entity.id)}
			<EntityContainer bind:entity />
		{/each}
	</div>
</div>

